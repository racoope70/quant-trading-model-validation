        if source_mode == "historical_fallback":
            f.write(
                "NOTE: Current/latest run had no summary files. "
                "This report was generated from historical run summaries.\n\n"
            )

        f.write("Summary files used:\n")
        for p in summary_files:
            f.write(f"- {p}\n")

        f.write("\n")

        if summary_df.empty:
            f.write("No usable summary rows found.\n")
            print(f"[ANALYSIS] Report written but no usable rows -> {report_path}")
            return report_path

        f.write(f"Total usable rows: {len(summary_df)}\n")

        if "Prefix" in summary_df.columns:
            f.write(f"Unique model/window rows: {summary_df['Prefix'].nunique()}\n")

