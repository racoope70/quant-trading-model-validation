from concurrent.futures import ThreadPoolExecutor, as_completed

def format_eta(seconds: float) -> str:
    if seconds is None or not np.isfinite(seconds) or seconds < 0:
        return "unknown"

    minutes = seconds / 60.0
    if minutes < 60:
        return f"{minutes:.1f} min"

    hours = minutes / 60.0
    return f"{hours:.2f} hr"

def process_ticker(ticker: str) -> list[dict]:
    ticker_start = time.time()
    try:
        logging.info(f"[TICKER START] {ticker}")

        hp = pick_params(ticker)
        df_sym = df[df["Symbol"] == ticker].copy()

        results = walkforward_ppo(
            df_sym=df_sym,
            ticker=ticker,
            window_size=WINDOW_SIZE,
            step_size=STEP_SIZE,
            timesteps=TIMESTEPS,
            ppo_overrides=hp,
        )

        elapsed = time.time() - ticker_start
        logging.info(
            f"[TICKER DONE] {ticker} | "
            f"windows={len(results)} | "
            f"runtime={format_eta(elapsed)}"
        )
        return results

    except Exception as e:
        elapsed = time.time() - ticker_start
        logging.error(
            f"{ticker}: training failed after {format_eta(elapsed)}: {e}",
            exc_info=True
        )
        return []

def run_parallel_tickers(tickers: list[str], out_path: str, max_workers: int):
    logging.info(f"[PARALLEL] max_workers={max_workers}")

    results = []
    total = len(tickers)
    run_start = time.time()
    completed = 0

    with ThreadPoolExecutor(max_workers=max_workers) as ex:
        future_to_ticker = {
            ex.submit(process_ticker, ticker): ticker
            for ticker in tickers
        }

        for future in as_completed(future_to_ticker):
            ticker = future_to_ticker[future]
            completed += 1

            try:
                res = future.result()
                if res:
                    results.extend(res)
            except Exception as e:
                logging.error(f"[PROGRESS] {ticker} failed in future: {e}", exc_info=True)

            elapsed = time.time() - run_start
            avg_per_ticker = elapsed / max(completed, 1)
            remaining = total - completed
            eta = avg_per_ticker * remaining

            logging.info(
                f" [TICKER PROGRESS] completed={completed}/{total} | "
                f"last={ticker} | "
                f"remaining={remaining} | "
                f"elapsed={format_eta(elapsed)} | "
                f"ETA≈{format_eta(eta)}"
            )

    if os.path.exists(SUMMARY_INCREMENTAL_PATH):
        final_df = pd.read_csv(SUMMARY_INCREMENTAL_PATH)

        if "Prefix" in final_df.columns:
            final_df = final_df.drop_duplicates(subset=["Prefix"], keep="last")

        final_df.to_csv(out_path, index=False)
        logging.info(f"Saved final summary from incremental -> {out_path}")

    elif results:
        pd.DataFrame(results).to_csv(out_path, index=False)
        logging.info(f"Saved summary from memory -> {out_path}")

    else:
        logging.warning("No results produced; summary not written.")

    return results

