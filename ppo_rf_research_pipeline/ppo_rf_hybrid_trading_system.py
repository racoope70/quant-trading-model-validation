# Out-of-sample evaluation utilities
def sharpe_from_returns(ret_s: pd.Series) -> float:
    r = pd.Series(ret_s).replace([np.inf, -np.inf], np.nan).fillna(0.0)
    if len(r) == 0:
        return 0.0
    return float((r.mean() / (r.std() + 1e-9)) * _annualization_factor())

def max_drawdown_from_nav(nav_track: list[float]) -> float:
    if len(nav_track) == 0:
        return 0.0
    nav_s = pd.Series(nav_track, dtype=float)
    dd = ((nav_s.cummax() - nav_s) / nav_s.cummax().replace(0, np.nan)).fillna(0.0)
    return float(dd.max() * 100.0)

def block_bootstrap_indices(n: int, block_size: int) -> np.ndarray:
    if n <= 0:
        return np.array([], dtype=int)
    idx = []
    while len(idx) < n:
        start = np.random.randint(0, max(1, n))
        block = [(start + i) % n for i in range(block_size)]
        idx.extend(block)
    return np.array(idx[:n], dtype=int)

def bootstrap_ci_from_returns(
    ret_s: pd.Series,
    b: int = 500,
    alpha: float = 0.05,
    block_size: int = 24,
) -> dict:
    r = pd.Series(ret_s).replace([np.inf, -np.inf], np.nan).fillna(0.0).values.astype(float)
    n = len(r)

    if n < 10:
        total_ret = float(np.prod(1.0 + r) - 1.0) if n else 0.0
        shp = sharpe_from_returns(pd.Series(r))
        return {
            "n_obs": int(n),
            "bootstrap_b": int(b),
            "alpha": float(alpha),
            "block_size": int(block_size),
            "return_mean_boot": total_ret,
            "return_ci_low": total_ret,
            "return_ci_high": total_ret,
            "sharpe_mean_boot": shp,
            "sharpe_ci_low": shp,
            "sharpe_ci_high": shp,
        }
    total_returns = []
    sharpes = []
    for _ in range(int(b)):
        idx = block_bootstrap_indices(n, block_size)
        rs = r[idx]
        total_returns.append(float(np.prod(1.0 + rs) - 1.0))
        sharpes.append(float(sharpe_from_returns(pd.Series(rs))))
    lo = alpha / 2.0
    hi = 1.0 - (alpha / 2.0)

    return {
        "n_obs": int(n),
        "bootstrap_b": int(b),
        "alpha": float(alpha),
        "block_size": int(block_size),
        "return_mean_boot": float(np.mean(total_returns)),
        "return_ci_low": float(np.quantile(total_returns, lo)),
        "return_ci_high": float(np.quantile(total_returns, hi)),
        "sharpe_mean_boot": float(np.mean(sharpes)),
        "sharpe_ci_low": float(np.quantile(sharpes, lo)),
        "sharpe_ci_high": float(np.quantile(sharpes, hi)),
    }

