def _summary_files_for_analysis(base_dir, current_run_folder=None, force_all_history=True):
    """
    Use all historical summaries by default so split/continued runs are analyzed together.
    This avoids false 'only UNH rows' analysis when earlier tickers were skipped or completed
    in earlier run folders.
    """
    historical_files = sorted(
        glob.glob(
            os.path.join(
                base_dir,
                "ppo_walkforward_results_*",
                "summary*.csv"
            )
        )
    )

    if force_all_history:
        return historical_files, "all_history"

    current_files = []
    if current_run_folder and os.path.exists(current_run_folder):
        current_files = sorted(
            glob.glob(os.path.join(current_run_folder, "summary*.csv"))
        )

    if current_files:
        return current_files, "current_run"

    return historical_files, "historical_fallback"


def _load_summary_files(summary_files):
    frames = []

    for p in summary_files:
        try:
            df_part = pd.read_csv(p)

            if df_part.empty:
                continue

            df_part["_source_summary_file"] = p
            df_part["_source_run_folder"] = os.path.basename(os.path.dirname(p))
            frames.append(df_part)

        except Exception as e:
            print(f"[ANALYSIS] Could not read summary file: {p} | {e}")

    if not frames:
        return pd.DataFrame()

    df = pd.concat(frames, ignore_index=True)

    # De-duplicate completed windows across final and incremental summaries.
    if "Prefix" in df.columns:
        df = df.drop_duplicates(subset=["Prefix"], keep="last")

    return df


def write_analysis_report(base_dir, report_run_folder):
    if not report_run_folder:
        print("[ANALYSIS] No run folder found; cannot write report.")
        return None

    summary_files, source_mode = _summary_files_for_analysis(
        base_dir=base_dir,
        current_run_folder=report_run_folder,
        force_all_history=True
    )

    summary_df = _load_summary_files(summary_files)

    report_path = os.path.join(report_run_folder, "analysis_report.txt")

    with open(report_path, "w") as f:
        f.write("PPO Analysis Report\n")
        f.write("===================\n\n")

