# Trade gate training and inference utilities
def build_trade_gate_label(df_in: pd.DataFrame, horizon: int, ret_thr: float) -> pd.DataFrame:
    df_local = df_in.copy()

    future_closes = pd.concat(
        [df_local["Close"].shift(-k) for k in range(1, horizon + 1)],
        axis=1
    )
    df_local["Gate_Future_Max_Close"] = future_closes.max(axis=1)
    df_local["Gate_Future_Max_Return"] = (
        df_local["Gate_Future_Max_Close"].sub(df_local["Close"]).div(df_local["Close"])
    )
    df_local["Trade_Label"] = (df_local["Gate_Future_Max_Return"] > ret_thr).astype(int)
    return df_local

