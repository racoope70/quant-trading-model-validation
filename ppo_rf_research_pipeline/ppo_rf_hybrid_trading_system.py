            if len(df_window) <= 52 or (len(df_window) % 2 != 0):
                df_window = df_window.iloc[:-1].reset_index(drop=True)

            split_for_gate = get_train_eval_split_idx(len(df_window))
            df_gate_train_part = df_window.iloc[:split_for_gate].copy()
            df_gate_eval_part = df_window.iloc[split_for_gate:].copy()
