# Incremental summary writer
def append_summary_incremental(row: dict, path: str = SUMMARY_INCREMENTAL_PATH):
    """
    Safely saves one completed window immediately.
    This protects results if Colab disconnects or the run is interrupted.
    """
    with SUMMARY_LOCK:
        df_row = pd.DataFrame([row])
        tmp_path = path + ".tmp"

        if os.path.exists(path):
            try:
                old = pd.read_csv(path)
                combo = pd.concat([old, df_row], ignore_index=True)

                if "Prefix" in combo.columns:
                    combo = combo.drop_duplicates(subset=["Prefix"], keep="last")

                combo.to_csv(tmp_path, index=False)
            except Exception:
                # Fallback: if old file is damaged, preserve it and start a recovery file
                recovery_path = path.replace(".csv", "_recovery.csv")
                df_row.to_csv(recovery_path, mode="a", index=False, header=not os.path.exists(recovery_path))
                logging.warning(f"[SUMMARY_INCREMENTAL] Main file issue; wrote recovery row -> {recovery_path}")
                return
        else:
            df_row.to_csv(tmp_path, index=False)

        os.replace(tmp_path, path)
        logging.info(f"[SUMMARY_INCREMENTAL] Saved/updated row -> {path}")

# Skipped-window audit logging
def record_skips_global(
    ticker: str,
    skipped_windows: list[str],
    total_windows: int | None = None,
    fully_skipped: bool = False,
):
    if not skipped_windows and not fully_skipped:
        return
    with SKIP_LOCK:
        new_file = not os.path.exists(SKIP_AGG_PATH)
        with open(SKIP_AGG_PATH, "a", newline="") as f:
            w = csv.writer(f)
            if new_file:
                w.writerow(["Ticker", "Window", "FullySkipped", "TotalWindows"])

            if fully_skipped:
                w.writerow([ticker, "ALL", True, total_windows if total_windows is not None else ""])
            else:
                for wname in skipped_windows:
                    win = ""
                    try:
                        _, win_str = wname.split("_window")
                        win = int(win_str)
                    except Exception:
                        pass
                    w.writerow([ticker, win, False, total_windows if total_windows is not None else ""])

# Volatility forecast utilities
def _safe_series(x, name: str) -> pd.Series:
    s = pd.Series(x, copy=True)
    s = pd.to_numeric(s, errors="coerce")
    s.name = name
    return

def realized_vol_proxy(ret_s: pd.Series) -> pd.Series:
    r = _safe_series(ret_s, "ret").fillna(0.0)
    return (r ** 2).shift(-1)

def rolling_vol_forecast(ret_s: pd.Series, lookback: int) -> pd.Series:
    r = _safe_series(ret_s, "ret").fillna(0.0)
    return r.rolling(lookback).std()

def ewm_vol_forecast(ret_s: pd.Series, span: int) -> pd.Series:
    r = _safe_series(ret_s, "ret").fillna(0.0)
    return r.ewm(span=span, adjust=False).std()

def garch_vol_forecast(
    ret_s: pd.Series,
    p: int = 1,
    q: int = 1,
    dist: str = "normal",
    refit_every: int = 25,
    min_train_size: int = 120,
) -> pd.Series:
    r = _safe_series(ret_s, "ret")
    out = pd.Series(np.nan, index=r.index, name="garch_vol")

    if (not ARCH_AVAILABLE) or len(r.dropna()) < min_train_size:
        return out
    start_idx = max(min_train_size, p + q + 5)
    start_idx = min(start_idx, len(r) - 2)
    if start_idx < min_train_size:
        return out

    r_pct = (r.fillna(0.0) * 100.0).astype(float)
    last_vol = np.nan

    for i in range(start_idx, len(r_pct)):
        should_refit = ((i - start_idx) % refit_every == 0) or (not np.isfinite(last_vol))
        if should_refit:
            hist = r_pct.iloc[:i].dropna()
            if len(hist) < min_train_size:
                continue
            try:
                am = arch_model(
                    hist,
                    mean="Zero",
                    vol="GARCH",
                    p=p,
                    q=q,
                    dist=dist,
                    rescale=False,
                )
                res = am.fit(disp="off", show_warning=False)
                fcast = res.forecast(horizon=1, reindex=False)
                var_1 = float(fcast.variance.values[-1, 0]) / (100.0 ** 2)
                var_1 = max(var_1, 0.0)
                last_vol = np.sqrt(var_1)
            except Exception:
                last_vol = np.nan
        out.iloc[i] = last_vol
    return out

def qlike_loss(realized_var: pd.Series, forecast_var: pd.Series) -> float:
    rv = pd.to_numeric(realized_var, errors="coerce")
    fv = pd.to_numeric(forecast_var, errors="coerce")
    tmp = pd.DataFrame({"rv": rv, "fv": fv}).dropna()
    if tmp.empty:
        return np.inf
    eps = 1e-12
    rv_ = tmp["rv"].clip(lower=eps)
    fv_ = tmp["fv"].clip(lower=eps)
    return float(np.mean(np.log(fv_) + (rv_ / fv_)))

def mse_loss(realized_var: pd.Series, forecast_var: pd.Series) -> float:
    rv = pd.to_numeric(realized_var, errors="coerce")
    fv = pd.to_numeric(forecast_var, errors="coerce")
    tmp = pd.DataFrame({"rv": rv, "fv": fv}).dropna()
    if tmp.empty:
        return np.inf
    return float(np.mean((tmp["rv"] - tmp["fv"]) ** 2))

def build_vol_forecast_candidates(
    df_window: pd.DataFrame,
    lookback: int,
    ewm_span: int,
    train_frac: float,
    oos_start_idx: int | None = None,
) -> tuple[pd.DataFrame, dict]:
    """
    Select the volatility forecast using only the in-window training segment.
    The held-out OOS segment is excluded from model-selection decisions.
    """
    df_local = df_window.copy()
    if "Close" not in df_local.columns:
        raise ValueError("build_vol_forecast_candidates: missing Close")
    df_local["Ret_1"] = pd.to_numeric(df_local["Close"], errors="coerce").pct_change().fillna(0.0)
    ret_s = df_local["Ret_1"].copy()
    realized_var = realized_vol_proxy(ret_s)
    candidate_map = {
        "rolling": rolling_vol_forecast(ret_s, lookback=lookback),
        "ewm": ewm_vol_forecast(ret_s, span=ewm_span),
    }

    if "garch" in VOL_FORECAST_CANDIDATES:
        candidate_map["garch"] = garch_vol_forecast(
            ret_s,
            p=VOL_FORECAST_GARCH_P,
            q=VOL_FORECAST_GARCH_Q,
            dist=VOL_FORECAST_GARCH_DIST,
            refit_every=VOL_FORECAST_GARCH_REFIT_EVERY,
            min_train_size=VOL_FORECAST_GARCH_MIN_TRAIN,
        )
    else:
        candidate_map["garch"] = pd.Series(np.nan, index=df_local.index)

    df_local["Vol_Roll"] = maybe_annualize_vol(candidate_map["rolling"])
    df_local["Vol_EWM"] = maybe_annualize_vol(candidate_map["ewm"])
    df_local["Vol_GARCH"] = maybe_annualize_vol(candidate_map["garch"])

    if oos_start_idx is None:
        selection_end = len(df_local)
    else:
        selection_end = max(min(int(oos_start_idx), len(df_local)), 61)

    split_idx = max(int(selection_end * train_frac), 60)
    split_idx = min(split_idx, selection_end - 1)
    score_fn = qlike_loss if VOL_FORECAST_SELECTION_METRIC.lower() == "qlike" else mse_loss
    scores = {}
    score_coverage = {}

    for name in VOL_FORECAST_CANDIDATES:
        vol_s = candidate_map.get(name, pd.Series(np.nan, index=df_local.index))
        var_s = (vol_s ** 2)

        rv_slice = pd.to_numeric(realized_var.iloc[split_idx:selection_end], errors="coerce")
        fv_slice = pd.to_numeric(var_s.iloc[split_idx:selection_end], errors="coerce")
        valid_mask = rv_slice.notna() & fv_slice.notna()

        coverage = int(valid_mask.sum())
        score_coverage[name] = coverage

        if coverage < 50:
            scores[name] = np.inf
        else:
            scores[name] = score_fn(
                rv_slice[valid_mask],
                fv_slice[valid_mask],
            )

    baseline_name = VOL_FORECAST_BASELINE if VOL_FORECAST_BASELINE in scores else "ewm"
    baseline_score = scores.get(baseline_name, np.inf)
    best_method = baseline_name
    best_score = baseline_score

    for name, sc in scores.items():
        if np.isfinite(sc) and (sc < best_score):
            best_method = name
            best_score = sc
    improvement = baseline_score - best_score if np.isfinite(baseline_score) and np.isfinite(best_score) else 0.0

    if best_method != baseline_name and improvement < VOL_FORECAST_MIN_IMPROVEMENT:
        best_method = baseline_name
        best_score = baseline_score
    selected_series = {
        "rolling": df_local["Vol_Roll"],
        "ewm": df_local["Vol_EWM"],
        "garch": df_local["Vol_GARCH"],
    }.get(best_method, df_local["Vol_EWM"])

    selected_series = pd.to_numeric(selected_series, errors="coerce")
    selected_series = selected_series.fillna(df_local["Vol_EWM"])
    selected_series = selected_series.fillna(df_local["Vol_Roll"])
    selected_series = selected_series.fillna(0.0)
    df_local["Vol_Forecast"] = selected_series
    df_local["Vol_Method_Selected"] = best_method
    vol_ref = float(df_local["Vol_Forecast"].median()) if len(df_local) else 0.0
    vol_gate = float(df_local["Vol_Forecast"].quantile(VOL_GATE_Q)) if len(df_local) else 0.0
    df_local["Vol_Ref"] = vol_ref
    df_local["Vol_Gate"] = vol_gate

    diagnostics = {
        "selected_method": best_method,
        "selection_metric": VOL_FORECAST_SELECTION_METRIC,
        "scores": {k: (None if not np.isfinite(v) else float(v)) for k, v in scores.items()},
        "score_coverage": score_coverage,
        "baseline_method": baseline_name,
        "baseline_score": None if not np.isfinite(baseline_score) else float(baseline_score),
        "best_score": None if not np.isfinite(best_score) else float(best_score),
        "improvement_vs_baseline": float(improvement) if np.isfinite(improvement) else None,
        "arch_available": bool(ARCH_AVAILABLE),
        "vol_ref": float(vol_ref),
        "vol_gate": float(vol_gate),
        "split_idx": int(split_idx),
        "selection_end": int(selection_end),
    }
    return df_local, diagnostics

