                    step_df = base_eval["step_df"]
                    trades = base_eval["trades"]
                    nav_track = base_eval["nav_track"]
                    bh_track = base_eval["bh_track"]
                    model = model_base
                    env = env_base
                    selected_feature_source = "BASE"

